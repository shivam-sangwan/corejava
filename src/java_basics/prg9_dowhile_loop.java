package java_basics;

public class prg9_dowhile_loop {

	public static void main(String[] args) 
	{
		//do while
				int b =1;
				do
				{
					System.out.println(b);
					b++;
				}while(b<=5);
				
		//do while will execute once even for false condition
				System.out.println("switch");
				b =11;
				do
				{
					System.out.println(b);
					b++;
				}while(b<=10);

	}

}
